// 7. Write a program to print 1 to 10 using the do-while loop statement.

public class DoWhile1To10 {
	public static void main(String args[]){
        int i=1;
        System.out.println("Numbers from 1 to 10 are:");
        do{
            System.out.println(i);
            i++;
        }while(i<=10);
    }
}
