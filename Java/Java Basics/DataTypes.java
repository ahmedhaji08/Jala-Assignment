// 4. Define variables for different Data Types int, Boolean, char, float, double and print on the Console

public class DataTypes{
    public static void main(String[] args) {
        int a = 10; //Default size is 4 bytes
        float b = 20.5f; //Default size is 4 bytes
        double c = 30.5;    //Default size is 8 bytes
        char d = 'A';   //Default size is 2 bytes
        boolean e = true; //Default size is 1 byte
        System.out.println("This is an Integer: "+a);
        System.out.println("This is a Float: "+b);
        System.out.println("This is a Character: "+c);
        System.out.println("This is a Double: "+d);
        System.out.println("This is a Boolean: "+e);
    }
}